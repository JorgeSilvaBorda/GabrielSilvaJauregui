
DROP TABLE IF EXISTS `PRODUCTO`;
CREATE TABLE `PRODUCTO` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `NOMBRE` varchar(500) NOT NULL,
  `PRECIO` int NOT NULL,
  `CANTIDAD` int NOT NULL,
  PRIMARY KEY (`ID`),
  UNIQUE KEY `NOMBRE` (`NOMBRE`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

DROP TABLE IF EXISTS `VENDEDOR`;
CREATE TABLE `VENDEDOR` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `RUT` varchar(10) NOT NULL,
  `NOMBRES` varchar(200) NOT NULL,
  `APPATERNO` varchar(200) NOT NULL,
  `APMATERNO` varchar(200) NOT NULL,
  PRIMARY KEY (`ID`),
  UNIQUE KEY `RUT` (`RUT`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

DROP TABLE IF EXISTS `VENTA`;
CREATE TABLE `VENTA` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `FECHA` date NOT NULL,
  `IDVENDEDOR` int NOT NULL,
  `TOTAL` int NOT NULL,
  PRIMARY KEY (`ID`),
  KEY `IDVENDEDOR` (`IDVENDEDOR`),
  CONSTRAINT `VENTA_ibfk_1` FOREIGN KEY (`IDVENDEDOR`) REFERENCES `VENDEDOR` (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

DROP TABLE IF EXISTS `VENTAPRODUCTO`;
CREATE TABLE `VENTAPRODUCTO` (
  `IDVENTA` int NOT NULL,
  `IDPRODUCTO` int NOT NULL,
  `CANTIDAD` int NOT NULL,
  `TOTAL` int NOT NULL,
  KEY `IDVENTA` (`IDVENTA`),
  KEY `IDPRODUCTO` (`IDPRODUCTO`),
  CONSTRAINT `VENTAPRODUCTO_ibfk_1` FOREIGN KEY (`IDVENTA`) REFERENCES `VENTA` (`ID`),
  CONSTRAINT `VENTAPRODUCTO_ibfk_2` FOREIGN KEY (`IDPRODUCTO`) REFERENCES `PRODUCTO` (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;
